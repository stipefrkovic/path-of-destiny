package nl.rug.oop.npc;

public abstract class NPC {

    private String name;
    private String type;
    private int health;
    private float strength;

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public int getHealth() {
        return health;
    }

    public float getStrength() {
        return strength;
    }

    
}
